import time
N1 = float(6)
N2 = float(2)

# Adição
print("Adição: ", N1 + N2)

#Subtração
print("Subtração: ", N1 - N2)

#Multiplicação
print("Multiplicação: ", N1 * N2)

#Divisão
print("Divisão: ", N1 / N2)

#Divisão inteira
print("Divisão inteira: ", N1 // N2)

#Resto da divisão
print("Resto da divisão: ", N1 % N2)

#Potência
print("Potência: ", N1 ** N2)

time.sleep(5)